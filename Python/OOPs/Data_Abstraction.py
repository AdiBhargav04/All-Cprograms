# DATA ABSTRACTION
# Abstraction is used to hide the onternal fucntionality 
# from the users
# The user only interacts with basic interface


# Abstraction allows us to create a clear distinction between 
# the interface and actual implementation

# In Pyhton, Data Abstraction is done done the use of abstract classes.

# What is abstract classes?
# A class that consists of one or more abstract method is called abstract class
# Abstract methods do not contain the implementation
# Abstract class can be inherited by the derived class
# Abstract method is defined in the derived class

# Python doesnot support the abstract class itself
# We need to import the abc module which provides the
# base for defining Abstract Base Class (ABC)

# Eg:

# An Abstract class cant be instantiated. we cant create objects for the abstract class

